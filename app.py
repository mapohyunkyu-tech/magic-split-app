    for c in ["Close", "Amount", "Marcap", "Volume"]:
        krx[c] = pd.to_numeric(krx[c], errors="coerce").fillna(0)

    krx = krx.drop_duplicates(subset=["Code"], keep="first").reset_index(drop=True)

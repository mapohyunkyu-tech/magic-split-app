def parse_won(x):
    s = str(x).strip().replace(",", "").replace("원", "").replace(" ", "")
    if s == "":
        return 0
    neg = False
    if s.startswith("-"):
        neg = True
        s = s[1:]
    total = 0
    if "억" in s:
        a, b = s.split("억", 1)
        if a:
            total += float(a) * 100_000_000
        s = b
    if "만" in s:
        a = s.replace("만", "")
        if a:
            total += float(a) * 10_000
    else:
        if total == 0 and s:
            total += float(s)
    total = int(total)
    return -total if neg else total



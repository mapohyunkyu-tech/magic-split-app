def _safe_int_value(v, default=0):
    try:
        if v is None or str(v).strip() == "":
            return int(default)
        return int(float(str(v).replace(",", "")))
    except Exception:
        return int(default)


def won_input_value(n):
    try:
        return f"{int(n):,}"
    except Exception:
        return str(n)


